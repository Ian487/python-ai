for i in range(10):  # i是迴圈的變數, range(10)是從0到9的數字
    print(i)  # i 每次只從range(10)中取出一個數字, 從0到9
for i in range(2, 100):  # range(2, 100)是從2到99的數字
    print(i)  # i 每次只從range(2, 100)中取出一個數字, 從2到99
for i in range(2, 100, 2):  # range(2, 100, 2)是從2到99的數字, 每次取出的是偶數
    print(i)  # i 每次只從range(2, 100, 2)中取出一個數字, 從2到99的偶數
for i in range(100, 3, -2):  # range(100, 3, -2)是從100到4的數字, 每次取出的是偶數
    print(i)  # i 每次只從range(100, 3, -2)中取出一個數字, 從100到4的偶數
